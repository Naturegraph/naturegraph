/**
 * Données mockées pour les partages (posts)
 * Structure extensible avec deux types distincts : Rencontre nature et Instant nature
 */

import { simulateNetworkDelay, calculatePagination } from '@/constants/config'

export type PostType = 'nature_encounter' | 'nature_instant'

export type TimeOfDay =
  | 'morning' // Matinée
  | 'afternoon' // Après-midi
  | 'dusk' // Crépuscule
  | 'evening' // Soirée
  | 'night' // Nuit

export type WeatherCondition =
  | 'sunny' // Ensoleillé
  | 'cloudy' // Nuageux
  | 'rainy' // Pluvieux
  | 'windy' // Venteux
  | 'snowy' // Neigeux

export type HabitatType =
  | 'forest' // Forêt
  | 'park_garden' // Parc & jardin
  | 'prairie_heath' // Prairie & lande
  | 'urban' // Milieu urbain
  | 'river' // Rivière
  | 'lake_wetland' // Lac & zone humide
  | 'mountain' // Montagne
  | 'sea_coast' // Mer & littoral

export type TaxonomicGroup =
  | 'birds'
  | 'mammals'
  | 'insects'
  | 'amphibians'
  | 'reptiles'
  | 'arachnids'
  | 'mollusks'
  | 'fish'
  | 'plants'
  | 'other'

/**
 * Champs communs à tous les types de partages
 */
interface PostBase {
  id: string
  userId: string
  type: PostType
  description: string
  location: {
    city: string
    region: string
    country: string
  }
  encounterDate: string // ISO date
  timeOfDay: TimeOfDay
  weather: WeatherCondition
  habitat: HabitatType
  multipleObservations: boolean
  createdAt: string // ISO date
  // Stats
  likesCount: number
  commentsCount: number
  sharesCount: number
}

/**
 * Rencontre nature : observation d'une espèce animale ou végétale
 */
export interface NatureEncounter extends PostBase {
  type: 'nature_encounter'
  speciesIdentified: boolean
  speciesName?: string
  taxonomicGroup?: TaxonomicGroup
}

/**
 * Instant nature : paysage ou phénomène naturel (aucun être vivant observé)
 */
export interface NatureInstant extends PostBase {
  type: 'nature_instant'
  phenomenon?: string // Ex: "Coucher de soleil", "Arc-en-ciel", "Brouillard matinal"
}

/**
 * Type union pour tous les posts
 */
export type Post = NatureEncounter | NatureInstant

