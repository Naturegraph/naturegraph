/**
 * Fonction utilitaire pour récupérer un post par son ID
 */
export async function fetchPostById(id: string): Promise<Post | undefined> {
  await simulateNetworkDelay()
  return mockPosts.find((post) => post.id === id)
}

/**
 * Fonction utilitaire pour récupérer les posts d'un utilisateur
 */
export async function fetchPostsByUserId(userId: string): Promise<Post[]> {
  await simulateNetworkDelay()
  return mockPosts.filter((post) => post.userId === userId)
}

/**
 * Fonction utilitaire pour récupérer les posts par type
 */
export async function fetchPostsByType(type: PostType): Promise<Post[]> {
  await simulateNetworkDelay()
  return mockPosts.filter((post) => post.type === type)
}

/**
 * Fonction utilitaire pour récupérer les posts paginés
 */
